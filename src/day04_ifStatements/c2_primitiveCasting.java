package day04_ifStatements;

public class c2_primitiveCasting {
    public static void main(String[] args) {

        // PRIMITIVE CASTINGS
        //implicit caating-automatically
        //explicit caating -manual
        //double > float > long...
        byte b1 = 20;// -128 to 127

        long l1 = b1;//this is implicit casting automaticxally by java

        int i1= 10;
        byte b2 = (byte) i1; //explicit casting,this is will done by you manually

        double d1 = 2.3;
        float f1= (float) d1;// explicit casting

        int i2 = 200;
        long l2 = i2;//implicit casting

        short s = 155;//this is just short number










    }
}
